#include <iostream>
using namespace std;
int main()
{
    int n,a,b,c;
    b=1;c=1;
    cout<<"Nhập số dòng : ";cin>>n;
    while(c<=n) {
        a=c;b=1;
        while (b<=c) {
            cout<<(a++%10);
            b++;
        }
        b=b-2;a=a-2;
        while (b>=1) {
            cout<<(a--%10);
            b--;
        }
        cout<<endl;
        c++;
    }
    return 0;
}

